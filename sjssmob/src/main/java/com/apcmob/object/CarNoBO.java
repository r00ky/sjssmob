package com.apcmob.object;

public class CarNoBO {

	private String carNo      ;
	private String carName    ;
	private String driverName ;
	private String driverHpNo ;
	private String placeCode ;
	private String carryQty ;
	
	public String getCarNo() {
		return carNo;
	}
	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	public String getDriverHpNo() {
		return driverHpNo;
	}
	public void setDriverHpNo(String driverHpNo) {
		this.driverHpNo = driverHpNo;
	}
	public String getPlaceCode() {
		return placeCode;
	}
	public void setPlaceCode(String placeCode) {
		this.placeCode = placeCode;
	}
	public String getCarryQty() {
		return carryQty;
	}
	public void setCarryQty(String carryQty) {
		this.carryQty = carryQty;
	}
}
