package com.apcmob.object;

public class FinishSheet3 {

	private String dataKey  = "";
	private String dataName = "";
	private String openAmt  = "";
	private String inAmt    = "";
	private String outAmt   = "";
	private String closeAmt = "";
	public String getDataKey() {
		return dataKey;
	}
	public void setDataKey(String dataKey) {
		this.dataKey = dataKey;
	}
	public String getDataName() {
		return dataName;
	}
	public void setDataName(String dataName) {
		this.dataName = dataName;
	}
	public String getOpenAmt() {
		return openAmt;
	}
	public void setOpenAmt(String openAmt) {
		this.openAmt = openAmt;
	}
	public String getInAmt() {
		return inAmt;
	}
	public void setInAmt(String inAmt) {
		this.inAmt = inAmt;
	}
	public String getOutAmt() {
		return outAmt;
	}
	public void setOutAmt(String outAmt) {
		this.outAmt = outAmt;
	}
	public String getCloseAmt() {
		return closeAmt;
	}
	public void setCloseAmt(String closeAmt) {
		this.closeAmt = closeAmt;
	}
	@Override
	public String toString() {
		return "FinishSheet3 [dataKey=" + dataKey + ", dataName=" + dataName + ", openAmt=" + openAmt + ", inAmt="
				+ inAmt + ", outAmt=" + outAmt + ", closeAmt=" + closeAmt + "]";
	}
	
	
}
